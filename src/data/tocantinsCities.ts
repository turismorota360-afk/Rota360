export const getCityBySlug = (slug: string): City | undefined => {
  return tocantinsCities.find(city => city.slug === slug);
};

export const getAttractionsByCity = (cityId: string): Attraction[] => {
  return attractions.filter(attraction => attraction.cityId === cityId);
};

export const getFeaturedCities = (): City[] => {
  return tocantinsCities.filter(city => city.featured);
};
